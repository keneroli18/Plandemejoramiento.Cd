# En este ejemplo, se están utilizando la función "input" para pedir al usuario que introduzca sus datos.
nombre = input("¿Cuál es tu nombre? ")
edad = input("¿Cuántos años tienes? ")
altura = input("¿Cuánto mides? ")
# Por último, en las líneas siguientes se utiliza la función "print" para imprimir en pantalla los valores introducidos por el usuario.
print("Hola,", nombre)
print("Tienes", edad, "años")
print("Mides", altura, "metros")
