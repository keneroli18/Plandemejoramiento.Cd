x = 5
y = 10
# En este ejemplo, se están utilizando dos operadores de asignación: "+=" y "-=". Estos operadores realizan una operación aritmética y luego asignan el resultado a la variable correspondiente.
x += 2 # x = x + 2
y -= 3 # y = y - 3
# Por último, en las dos líneas siguientes se imprime en pantalla el valor actualizado de "x" e "y".
print("El valor de x es:", x)
print("El valor de y es:", y)